#ifndef GTOM_HH
#define GTOM_HH

#include <vector>
#include <limits.h>
#include <iostream>
#include <fstream>

#include <QString>
#include <QProcess>
#include <QObject>

#include "canonLine.hpp"

namespace g2m {

/**
\class g2m
\brief This class does the high level stuff for creating a model from gcode.
It runs the interpreter, and creates a canonLine object for each canonical 
* command generated by the interpreter. It also does error checking and 
* stores the cannonLines. It calls methods on each canonLine object to display it. 
* Eventually it will enable the user to see which gcode line creates which 
* bit of the path, and vice versa.
*/
class g2m : public QObject {
  Q_OBJECT;
  public:
    g2m()  { 
        debug=false;
    }
    void interpret_file();
    void setFile(std::string infile) {
        file = QString::fromStdString(infile);
    }
    void setToolTable(std::string tbl_file) {
        tooltable = QString::fromStdString(tbl_file);
    }
    void setInterp(std::string interp_binary) {
        interp = QString::fromStdString(interp_binary);
    }
    void setDebug(bool d) {debug=d;}
    std::vector<canonLine*> getCanonLines() {
        return lineVector;
    }
  protected:    
    bool chooseToolTable();
    void interpret();
    bool processCanonLine(std::string l);
    bool startInterp(QProcess &tc);
    void infoMsg(std::string s);

    std::vector<canonLine*> lineVector;
    QString file;
    QString tooltable;
    QString interp;
    bool debug;
};


} // end namespace
#endif //GTOM_HH
